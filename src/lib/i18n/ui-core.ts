export type AppTranslationKey = FrKey;

export function tApp(locale: string, key: AppTranslationKey, params?: Params): string {
  const normalized = (locale === "ar" || locale === "en" || locale === "fr" ? locale : "fr") as AppLocale;
  const base = APP_UI.fr[key] ?? key;
  const raw = normalized === "fr" ? base : ((APP_UI[normalized] as Partial<Record<FrKey, string>>)[key] ?? base);
  return interpolate(raw, params);
}

export function formatUiDate(value: string | number | Date, locale: string): string {
  return new Intl.DateTimeFormat(getIntlLocale(locale), {
    year: "numeric",
    month: "short",
    day: "numeric",
  }).format(new Date(value));
}

export function formatUiDateTime(value: string | number | Date, locale: string): string {
  return new Intl.DateTimeFormat(getIntlLocale(locale), {
    year: "numeric",
    month: "short",
    day: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  }).format(new Date(value));
}
